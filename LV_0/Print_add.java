package LV_0;

import java.util.Scanner;

public class Print_add {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a + " + " + b + " = " + (a + b));
        sc.close();
    }
}
       